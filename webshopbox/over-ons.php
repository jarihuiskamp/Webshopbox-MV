            <h1>Hoe werkt het?</h1>
            <p>
              Naast onze fysieke winkel hebben we ook een website met boxen. De boxen hebben altijd meer waarde qua geld dan dat je het los zou kopen,
              dus je krijgt altijd wat extra's. De inhoud van de box zal per keer ietsjes verschillen, ben je niet blij met een bepaald
              item in je box kan je het doorgeven aan ons via de mail en zullen we opletten als we je box vullen.
              Je hebt de keuze uit 3 verschillende boxen, je kan de Candy box kiezen, deze bestaat uit allerlij vershillende snoepsoorten,
              De Crispy box, die bestaat uit lekkere chips, koekjes en andere knapperige lekkernijen, je hebt ook de Healty box,
              Hier zitten allemaal lekkere snacks in die stukken gezonder zijn dan meeste alternatieven.
