            <h1>Wie zijn wij?</h1>
            <p>
              Snackbox is opgericht in 1984 door Hendrik Huiskamp in Hillegom. Hij kwam met het idee om snoep en snacks
              te gaan verkopen door zijn liefde ervoor. Toen hij vroeger als kleine jongen in een snoepwinkel stond wist hij
